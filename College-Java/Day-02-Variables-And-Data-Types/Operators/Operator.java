public class Operator {
    public static void main(String[] args) {
        int num1 = 10,num2 = 2;
        int sum = num1 + num2;
        System.out.println("1st Number :- " + num1);
        System.out.println("2nd Number :- " + num2);
        System.out.println("Sum Of Both "+ num1 +" & "+ num2 +" :- " + sum);
    }
}
